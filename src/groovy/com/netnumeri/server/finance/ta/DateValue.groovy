package com.netnumeri.server.finance.ta;


public class DateValue {

    Date date;
    double value;

    public DateValue() {
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}
